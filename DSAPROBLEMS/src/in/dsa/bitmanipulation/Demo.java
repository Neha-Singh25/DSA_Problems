package in.dsa.bitmanipulation;

public class Demo {
	public static void main(String[] args) {
		//Binary AND & 
		System.out.println(5 & 4);
		
		//Binary OR |
		System.out.println(5 | 6);
		
		//Binary XOR ^
		System.out.println(5 ^ 6);
		
		//Binary one's complement ~
		System.out.println(~5);
		
		//Binary Left Shift <<
		System.out.println(5 << 2);
		
		//Binary Right Shift >>
		System.out.println(6 >> 1);
	}
}
